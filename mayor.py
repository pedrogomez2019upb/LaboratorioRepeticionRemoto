#Algoritmo que permita determinar si un número entero es mayor a cero
#Primero vamos a crerar una variable X para que reciba un valor 
x=int(input("Bienvenido! Vamos a evaluar el número ingresado para saber si es mayor a cero. Por favor ingresa un número entero: "))

if x > 0:
    print ("El número {} es mayor a cero. Gracias por utilizar el programa.".format(x))
else:
    print ("El número {} es menor a cero. Gracias por utilizar el programa.".format(x))
